#!/usr/bin/env python3
"""中诚咨询培训管理制度 - 完整细化版（第二部分：第八至十七章）"""
from docx import Document
from docx.shared import Pt, RGBColor, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.table import WD_TABLE_ALIGNMENT, WD_CELL_VERTICAL_ALIGNMENT
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

doc = Document()

section = doc.sections[0]
section.page_width = Cm(21); section.page_height = Cm(29.7)
section.left_margin = Cm(2.8); section.right_margin = Cm(2.5)
section.top_margin = Cm(2.5); section.bottom_margin = Cm(2)

C_DARK=RGBColor(0x1A,0x3A,0x5C); C_MID=RGBColor(0x2E,0x6D,0xA4)
C_LIGHT=RGBColor(0xD4,0xE6,0xF5); C_WHITE=RGBColor(0xFF,0xFF,0xFF)
C_TEXT=RGBColor(0x1F,0x1F,0x1F); C_GREY=RGBColor(0x60,0x60,0x60)

def sf(run,name='宋体',size=10.5,bold=False,color=None,italic=False):
    run.font.name=name; run.font.size=Pt(size)
    run.font.bold=bold; run.font.italic=italic
    if color: run.font.color.rgb=color
def sp(p,before=2,after=5):
    p.paragraph_format.space_before=Pt(before)
    p.paragraph_format.space_after=Pt(after)
def shade(cell,hex_color):
    tc=cell._tc; tcPr=tc.get_or_add_tcPr()
    shd=OxmlElement('w:shd')
    shd.set(qn('w:val'),'clear'); shd.set(qn('w:color'),'auto')
    shd.set(qn('w:fill'),hex_color); tcPr.append(shd)
def h1(txt):
    p=doc.add_paragraph(); p.alignment=WD_ALIGN_PARAGRAPH.CENTER
    run=p.add_run(txt); sf(run,'黑体',18,bold=True,color=C_DARK)
    sp(p,before=20,after=6)
    p2=doc.add_paragraph(); p2.alignment=WD_ALIGN_PARAGRAPH.CENTER
    run2=p2.add_run('─'*52); sf(run2,'宋体',9,color=C_MID)
    sp(p2,before=0,after=4)
def h2(txt):
    p=doc.add_paragraph(); run=p.add_run('▌ '+txt)
    sf(run,'黑体',13,bold=True,color=C_DARK); sp(p,before=16,after=6)
def h3(txt):
    p=doc.add_paragraph(); run=p.add_run('▶ '+txt)
    sf(run,'黑体',11,bold=True,color=C_MID); sp(p,before=10,after=4)
def body(txt,indent=0,prefix=None):
    p=doc.add_paragraph(); p.paragraph_format.left_indent=Cm(indent)
    if prefix:
        r1=p.add_run(prefix); sf(r1,'宋体',10.5,bold=True,color=C_TEXT)
        r2=p.add_run(txt); sf(r2,'宋体',10.5,color=C_TEXT)
    else:
        run=p.add_run(txt); sf(run,'宋体',10.5,color=C_TEXT)
    sp(p,before=2,after=4); return p
def bul(txt,level=1,mark='●'):
    p=doc.add_paragraph()
    p.paragraph_format.left_indent=Cm(level*0.8)
    p.paragraph_format.first_line_indent=Cm(-0.4)
    run=p.add_run(f'{mark}  {txt}'); sf(run,'宋体',10.5,color=C_TEXT)
    sp(p,before=2,after=3)
def num(txt,level=1,n='①②③④⑤⑥⑦⑧'):
    p=doc.add_paragraph()
    p.paragraph_format.left_indent=Cm(level*0.8)
    p.paragraph_format.first_line_indent=Cm(-0.5)
    run=p.add_run(f'{n[level-1] if level<=len(n) else str(level)+"."}  {txt}')
    sf(run,'宋体',10.5,color=C_TEXT); sp(p,before=2,after=3)
def div():
    p=doc.add_paragraph(); run=p.add_run('━'*55)
    sf(run,'宋体',8,color=C_LIGHT); sp(p,before=6,after=6)
def br(): doc.add_paragraph()
def tbl(headers,rows,widths=None,hdr='1A3A5C'):
    t=doc.add_table(rows=1+len(rows),cols=len(headers))
    t.style='Table Grid'; t.alignment=WD_TABLE_ALIGNMENT.CENTER
    for i,h in enumerate(headers):
        c=t.rows[0].cells[i]; c.text=h
        c.vertical_alignment=WD_CELL_VERTICAL_ALIGNMENT.CENTER
        shade(c,hdr)
        for p in c.paragraphs:
            p.alignment=WD_ALIGN_PARAGRAPH.CENTER
            for r in p.runs: sf(r,'黑体',9.5,bold=True,color=C_WHITE)
            sp(p,before=4,after=4)
    for ri,row in enumerate(rows):
        bg='F0F5FA' if ri%2==0 else 'FFFFFF'
        for ci,v in enumerate(row):
            c=t.rows[ri+1].cells[ci]; c.text=str(v)
            c.vertical_alignment=WD_CELL_VERTICAL_ALIGNMENT.CENTER
            shade(c,bg)
            for p in c.paragraphs:
                p.alignment=WD_ALIGN_PARAGRAPH.CENTER if ci==0 else WD_ALIGN_PARAGRAPH.LEFT
                for r in p.runs: sf(r,'宋体',9.5,color=C_TEXT)
                sp(p,before=3,after=3)
    if widths:
        for row in t.rows:
            for ci,w in enumerate(widths): row.cells[ci].width=Cm(w)
    return t
def pb(): doc.add_page_break()

